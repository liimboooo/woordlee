/**
 * Pick a random word from the word list.
 * Uses a daily seed so the word changes each day (like real Wordle).
 */
export function getDailyWord() {
  const now = new Date();
  const start = new Date(2025, 0, 1); // Jan 1, 2025
  const diff = Math.floor((now - start) / (1000 * 60 * 60 * 24));
  return WORD_LIST[diff % WORD_LIST.length].toUpperCase();
}

/**
 * Evaluate a guess against the solution.
 * Returns an array of 5 statuses: 'correct' | 'present' | 'absent'
 * Correctly handles duplicate letters.
 */
export function evaluateGuess(guess, solution) {
  const result = Array(5).fill('absent');
  const solutionChars = solution.split('');
  const guessChars = guess.split('');
  const remaining = [...solutionChars];

  // First pass: mark correct letters (green)
  for (let i = 0; i < 5; i++) {
    if (guessChars[i] === solutionChars[i]) {
      result[i] = 'correct';
      remaining[i] = null; // Mark as used
    }
  }

  // Second pass: mark present letters (yellow)
  for (let i = 0; i < 5; i++) {
    if (result[i] === 'correct') continue;
    const idx = remaining.indexOf(guessChars[i]);
    if (idx !== -1) {
      result[i] = 'present';
      remaining[idx] = null; // Mark as used
    }
  }

  return result;
}
